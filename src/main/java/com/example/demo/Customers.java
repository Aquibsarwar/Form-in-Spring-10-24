package com.example.demo;

public class Customers {
	private int cname;
	private int cid;
	private int cemail;
	
	public int getCname() {
		return cname;
	}
	public void setCname(int cname) {
		this.cname = cname;
	}
	public int getCid() {
		return cid;
	}
	public void setCid(int cid) {
		this.cid = cid;
	}
	public int getCemail() {
		return cemail;
	}
	public void setCemail(int cemail) {
		this.cemail = cemail;
	}
	
	
}
